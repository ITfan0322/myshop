package com.xbboomOrder.bean;

public class Youhui {
	int id;
	String openid;
	String types;
	String low;
	int states;
	String tips;
	String up;
	String dates;
	String bigdate;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOpenid() {
		return openid;
	}
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	public String getTypes() {
		return types;
	}
	public void setTypes(String types) {
		this.types = types;
	}
	public String getLow() {
		return low;
	}
	public void setLow(String low) {
		this.low = low;
	}
	public int getStates() {
		return states;
	}
	public void setStates(int states) {
		this.states = states;
	}
	public String getTips() {
		return tips;
	}
	public void setTips(String tips) {
		this.tips = tips;
	}
	public String getUp() {
		return up;
	}
	public void setUp(String up) {
		this.up = up;
	}
	public String getDates() {
		return dates;
	}
	public void setDates(String dates) {
		this.dates = dates;
	}
	public String getBigdate() {
		return bigdate;
	}
	public void setBigdate(String bigdate) {
		this.bigdate = bigdate;
	}
	@Override
	public String toString() {
		return "Youhui [id=" + id + ", openid=" + openid + ", types=" + types + ", low=" + low + ", states=" + states
				+ ", tips=" + tips + ", up=" + up + ", dates=" + dates + ", bigdate=" + bigdate + "]";
	}
	
	
	
}
